importScripts('chess.min.js');
var game = new Chess();

self.onmessage = function(e) {
    game.move(e.data, {sloppy: true});
    playMove(game);
}

function playMove(game) {
    var possible = game.moves({ verbose: true });
    var move = possible[0];
    var moveValue = -999;
    for(var i=0; i<possible.length; i++) {
        possibleMove = possible[i];
        
        game.move(possibleMove);
        var boardValue = minmax(2, -999, 999, game);
        game.undo();
        if(boardValue > moveValue) {
            moveValue = boardValue;
            move = possibleMove;
        }
    }
    game.move(move);
    self.postMessage(dashFormat(move));
}

var minmax = function (depth, a, b, game) {
    if (depth === 0) return -evaluateBoard(game.board());
    
    var possible = game.moves({ verbose: true });
    
    if (game.turn == 'b') {
        var maxScore = -999;
        for (var i = 0; i < possible.length; i++) {
            game.move(possible[i]);
            maxScore = Math.max(maxScore, minmax(depth - 1, a, b, game));
            game.undo();
            
            a = Math.max(a, maxScore);
            if(b < a) return maxScore;
        }
        return maxScore;
    } else {
        var maxScore = 999;
        for (var i = 0; i < possible.length; i++) {
            game.move(possible[i]);
            maxScore = Math.min(maxScore, minmax(depth - 1, a, b, game));
            game.undo();
            
            b = Math.min(b, maxScore);
            if(b < a) return maxScore;
        }
        return maxScore;
    }
};

function dashFormat(move) {
    return move.from +"-"+move.to;
}

var evaluateBoard = function (board) {
    var eval = 0;
    for (var i = 0; i < 8; i++) {
        for (var j = 0; j < 8; j++) {
            eval = eval + getPieceValue(board[i][j], i ,j);
        }
    }
    return eval;
};

